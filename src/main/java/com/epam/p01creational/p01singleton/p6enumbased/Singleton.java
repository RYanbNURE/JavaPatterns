package com.epam.p01creational.p01singleton.p6enumbased;

public enum Singleton {
    // Єдиний елемент перечислення, який представляє єдиний екземпляр класу Singleton.
    INSTANCE;

    // Можливі додаткові поля або методи для екземпляру, якщо потрібно.
    public void doSomething() {
        System.out.println("Виконуємо дію з єдиним екземпляром Singleton.");
    }
}

/*
### Пояснення:
1. **Перечислення (enum)**: `Singleton` - це перечислення, яке містить єдиний елемент (`INSTANCE`). Це гарантує, що буде створено тільки один екземпляр класу Singleton.
2. **Конструктор**: Конструктор в перечисленні автоматично є приватним та не потребує параметрів. Це запобігає можливості створення нового екземпляру ззовні коду.
3. **Метод `doSomething()`**: Наведений метод демонструє, як можна визначити додаткові дії для єдиного екземпляру перечислення. В даному випадку, метод просто друкує повідомлення у консоль.

Цей підхід простий та ефективний і гарантує, що єдиний екземпляр буде створено тільки в одному потоці, коли перечислення буде вперше використано. Це робить його надійним та безпечним для
багатопотокового середовища.

 */